package ch11;

import java.awt.BorderLayout;
import java.awt.Container;
import java.awt.FlowLayout;

import javax.swing.ImageIcon;
import javax.swing.JFrame;
import javax.swing.JLabel;
import javax.swing.SwingConstants;

public class LabelEx extends JFrame {

	private JLabel textLabel, imageLabel, label;
	private ImageIcon beauty, normalIcon; 
	private BorderLayout bl;
	
	public LabelEx() {
		setTitle("레이블 예제");
		setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		Container c = getContentPane();
		c.setLayout(new FlowLayout());
		
		//문자열 레이블 생성
		textLabel = new JLabel("사랑합니다");
		
		//이미지 레이블 생성
		beauty = new ImageIcon("img/beauty.jpg");
		imageLabel = new JLabel(beauty); //이미지 레이블 생성
		
		//문자열과 이미지를 모두 가진 레이블 생성
		normalIcon = new ImageIcon("img/normalIcon.gif"); //이미지 로딩
		label = new JLabel("마음의 문을 열어보세요", normalIcon, SwingConstants.CENTER); //레이블 생성
		
		//컨텐트팬에 3개의 레이블 부착
		c.add(textLabel);
		c.add(imageLabel);
		c.add(label);
		
		setSize(400, 600);
		setVisible(true);
	}
	
	public static void main(String[] args) {
		new LabelEx();
	}

}

package quiz;

import java.util.Scanner;

// Scanner 쓰는 법은 인터넷에서 검색

// if문 은 조건을 걸어서 코드를 선택하는 기능이다

// if - else if - else 형식이다

// 0보다 작으면 "0보다 작습니다" 를 출력
// 10보다 작으면 "10보다 작습니다" 를 출력
// 10이상이면 "10이상 입니다" 를 출력

public class Quiz013 {

	public static void main(String[] args) {
		System.out.println("숫자를 입력하세요");
		Scanner sc = new Scanner(System.in);
		int num = sc.nextInt();
		if (num < 0) {
			System.out.println("0보다 작습니다");
		} else if (0 <= num && num < 10) {
			System.out.println("10보다 작습니다");
		} else if (10 <= num) {
			System.out.println("10이상 입니다.");
		}
	}
}
